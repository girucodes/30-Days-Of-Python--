# 2. Find an Euclidian distance between (2, 3) and (10, 8)

'''
Euclidean distance is simply the distance between two points.
A(x1,y1), B(x2,y2)
The formula of Euclidean distance is given by:
    Distance  = sqrt((x2-x2)**2 + (y2-y1)**2)
'''

# A(2,3), B(10,8)

Distance = ((10-2)**2 + (8-3)**2)**0.5

print('The Euclidian distance between (2, 3) and (10, 8) :- ',Distance)

